void setup() {
  pinMode(S_button, INPUT_PULLUP); // set the digital pin as input and enable internal pullup resistor
  attachInterrupt(digitalPinToInterrupt(S_button), ISR_Start, FALLING); // attached interrupt to call ISR_Reset()
  Serial.begin(9600);
  Wire.setClock(3400000);
  display.begin(SSD1306_SWITCHCAPVCC, 0x3C);
  display.display();
  
}

void loop() {
switch (Current_state){
  case S_Start:{
    Title();
    score = 0;
    TotalApples = 0;
    Apple_y_new = 1;
    if(ButtonOn == HIGH){
      Current_state = S_Play;
    }
  break;
  }

  case S_Play:{
    Play();
    if(ButtonOn == HIGH){
      Current_state = S_Pause;
    }
    if(CheckScore == HIGH){
      Current_state = S_Nextlevel;
    }
    if(CheckFinalScore == HIGH){
      Current_state = S_Score;
    }
  break;
  }

  case S_Pause:{
    ButtonOn = LOW;
    display.setTextSize(0); // Draw 2X-scale text
    display.setTextColor(FontColor);
    display.setCursor(40, 20);
    display.print(F("PAUSED")); 
    display.display();

    unsigned long currentMillis = millis();

    if (currentMillis - previousMillis >= interval) {
      previousMillis = currentMillis;
      if (FontColor == WHITE) {
        FontColor = BLACK;
      } else {
          FontColor = WHITE;
        }
    }
    
    if  (ButtonOn == HIGH){
      Current_state = S_Play;
    }
  break;
  }

  case S_Nextlevel:{
    CheckScore = LOW;
    if  (score >= TotalApples/2 ) {
      Congrats();
      if  (ButtonOn == HIGH){
        Current_state = S_Play;
      } 
    } else {
        display.clearDisplay();
        display.setTextSize(2); // Draw 2X-scale text
        display.setTextColor(FontColor);
        display.setCursor(40, 10);
        display.println(F("Game"));
        display.setCursor(40, 30);
        display.println(F("Over"));
        display.display();

        unsigned long currentMillis = millis();

        if (currentMillis - previousMillis >= interval) {
          previousMillis = currentMillis;
          if (FontColor == WHITE) {
            FontColor = BLACK;
          } else {
              FontColor = WHITE;
            }
        }

       if (ButtonOn == HIGH){
        Current_state = S_Start;
       }
      }
    break;
    }

  case S_Score:{
    CheckFinalScore = LOW;
    display.clearDisplay();
    display.setTextSize(1); // Draw 2X-scale text
    display.setTextColor(FontColor);
    display.setCursor(35, 10);
    display.print(F("Your Score"));
    display.setTextSize(2);
    display.setTextColor(FontColor);
    display.setCursor(55, 30);
    display.print((score));
    display.display();

    unsigned long currentMillis = millis();

    if (currentMillis - previousMillis >= interval) {
          previousMillis = currentMillis;
          if (FontColor == WHITE) {
            FontColor = BLACK;
          } else {
              FontColor = WHITE;
            }
        }
    if (ButtonOn == HIGH){
          Current_state = S_Start;
         }
        }
      break;
      }
}

